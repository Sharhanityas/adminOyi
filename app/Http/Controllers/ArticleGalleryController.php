    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $item = ArticleGallery::findOrFail($id);
        $item->delete();

        return redirect()->route('article_galleries.index');
    }
