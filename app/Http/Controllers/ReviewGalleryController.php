<?php

namespace App\Http\Controllers;

use App\Models\Review;
use App\Models\ReviewGallery;
use Illuminate\Http\Request;

class ReviewGalleryController extends Controller
{
    /**
     * Create a new controller instance.
     *
     * @return void
     */
    public function __construct()
    {
        $this->middleware('auth');
    }
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $items = ReviewGallery::with('review')->get();

        return view('pages.review_galleries.index')->with([
            'items' => $items
        ]);
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $events = Review::all();

        return view('pages.review_galleries.create')->with([
            'reviews' => $events
        ]);
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->all();
        $data['foto'] = $request->file('foto')->store('assets/review', 'public');
        ReviewGallery::create($data);
        return redirect()->route('review_galleries.index');
    }

    /**
     * Display the specified resource.
